DRAWTEXT SECTION DRAWTEXT
	CALL	VDCENABLE
	CALL	GFXOFF

	; SET COLORS
	MOV	R0,#VDC_COLOR
	MOV	A,#COL_BCK_BLACK | COL_GRD_WHITE
