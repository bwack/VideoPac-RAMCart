; THE USABLE RAM SPACE OF THE G7000RAM (0400H-0FFFH IN 4 BANKS).
; WHEN USING 19200 YOU HAVE TO SEND WITH 2 STOP BITS, THE G7000 IS TOO SLOW
; TO HANDLE 19200 WITH ONLY 1 STOP BIT.

